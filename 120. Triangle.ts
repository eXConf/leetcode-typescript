function minimumTotal(
  triangle: number[][],
  prevIdx = 0,
  layer = 0,
  memo: Map<string, number> = new Map(),
): number {
  const id = `${prevIdx}x${layer}`;

  if (memo.has(id)) return memo.get(id)!;
  if (layer === triangle.length) return 0;

  const current = triangle[layer];

  if (current.length === 1)
    return current[0] + minimumTotal(triangle, prevIdx, layer + 1, memo);

  const results: number[] = [current[prevIdx], current[prevIdx + 1]];

  if (layer + 1 < triangle.length) {
    results[0] += minimumTotal(triangle, prevIdx, layer + 1, memo);
    results[1] += minimumTotal(triangle, prevIdx + 1, layer + 1, memo);
  }

  const result = Math.min(...results);
  memo.set(id, result);
  return result;
}

